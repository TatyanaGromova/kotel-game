export function pickHumor(category: HumorCategory): string {
  const lines = PIPE_HUMOR[category]
  return lines[Math.floor(Math.random() * lines.length)]
}

export function pickPipeHumor(_kind: 'rotate'): string {
  return pickHumor('rotatePipe')
}

